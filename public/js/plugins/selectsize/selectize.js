<p>Selectize is an extensible <a href="http://jquery.com/">jQuery</a>-based custom &lt;select&gt; UI control. It's useful for tagging, contact lists, country selectors, and so on. It clocks in at around ~7kb (gzipped). The goal is to provide a solid &amp; usable experience with a clean and powerful API.</p>

